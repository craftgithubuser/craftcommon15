package com.project;

public class Javademoextra {
	public static void main(String[]args) {

	System.out.println("while loop");
	int i =1;
	while(i<=10) {
		System.out.print(i+" ");
		i++;
	}
	System.out.println();
	System.out.println("Do while loop");
	int j =1;
	do {
		System.out.print(j + " ");
		j++;
	}while(j<=10);
		
	System.out.println();
	System.out.println("for loop");
	for(int k=1; k<=10; k++) {
		System.out.print(k + " ");

		
		
		
	}
		
		
	}
}


